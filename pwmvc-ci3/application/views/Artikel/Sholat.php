                  <p>Sholat lima waktu merupakan ibadah yang wajib dikerjakan oleh semua umat Islam. Terdapat sejumlah keutamaan yang dapat diraih setiap Muslim yang menunaikannya.</p>
                  <ul class="list-group">
                      <li class="list-group-item"> Sholat berjamaah 27 derajat lebih baik. Dari Abdullah Ibnu Umar, Rasulullah ﷺ bersabda: <br> <br>
                          صَلَاةُ الْجَمَاعَةِ أَفْضَلُ مِنْ صَلَاةِ الْفَذِّ بِسَبْعٍ وَعِشْرِينَ دَرَجَةً <br> "Sholat berjamaah lebih utama dua puluh tujuh derajat daripada sholat sendirian.” (HR Bukhari). </li>
                      <li class="list-group-item">Pergi ke masjid untuk sholat wajib akan mendapatkan pahala yang besar. Pahala akan dihitung berdasarkan jarak yang ditempuh. Demikian mereka yang menunggu sholat lima waktu dari satu ke lainnya dengan ibadah akan diganjar pahala. <br><br>

                          عَنْ أَبِي مُوسَى، قَالَ: قَالَ رَسُولُ اللهِ صلى الله عليه وسلم: "إِنَّ أَعْظَمَ النَّاسِ أَجْرًا فِي الصَّلَاةِ أَبْعَدُهُمْ إِلَيْهَا مَمْشًى، فَأَبْعَدُهُمْ، وَالَّذِي يَنْتَظِرُ الصَّلَاةَ حَتَّى يُصَلِّيَهَا مَعَ الْإِمَامِ أَعْظَمَ أَجْرًا مِنَ الَّذِي يُصَلِّيَهَا ثُمَّ يَنَامُ" وَفِي رِوَايَةِ أَبِي كُرَيْبٍ: "حَتَّى يُصَلِّيَهَا مَعَ الْإِمَامِ فِي جَمَاعَةٍ" <br>

                          Dari Abu Musa RA, dia berkata, “Rasulullah SAW bersabda, 'Manusia paling besar pahalanya dalam sholat adalah yang paling jauh perjalanannya, lalu yang selanjutnya, dan seseorang yang menunggu sholat hingga melakukannya bersama imam, lebih besar pahalanya daripada yang melakukannya kemudian tidur.' Dan dalam suatu periwayatan Abu Kuraib disebutkan; 'Hingga dia tunaikan sholat bersama imam secara berjamaah.'"</li>
                      <li class="list-group-item">Disiapkan tempat di surga <br><br>

                          مَنْ غَدَا إِلَى الْمَسْجِدِ، أَوْ رَاحَ، أَعَدَّ اللهُ لَهُ فِي الْجَنَّةِ نُزُلًا، كُلَّمَا غَدَا، أَوْ رَاحَ

                          <br> "Barang siapa pergi ke masjid pada awal dan akhir siang, maka Allah akan menyiapkan baginya tempat dan hidangan di surga setiap kali dia pergi." (HR Bukhari dan Muslim).
                      </li>
                      <li class="list-group-item">Sholat berjamaah mengangkat derajat seorang hamba dan menghapus dosa <br><br>

                          مَنْ تَطَهَّرَ فِى بَيْتِهِ ثُمَّ مَشَى إِلَى بَيْتٍ مِنْ بُيُوتِ اللَّهِ لِيَقْضِىَ فَرِيضَةً مِنْ فَرَائِضِ اللَّهِ كَانَتْ خَطْوَتَاهُ إِحْدَاهُمَا تَحُطُّ خَطِيئَةً وَالأُخْرَى تَرْفَعُ دَرَجَةً

                          <br> “Barang siapa bersuci di rumahnya lalu dia berjalan menuju salah satu dari rumah Allah (yaitu masjid) untuk menunaikan kewajiban yang telah Allah wajibkan, maka salah satu langkah kakinya akan menghapuskan dosa dan langkah kaki lainnya akan meninggikan derajatnya.” (HR Bukhari dari Abu Hurairah RA).
                      </li>
                  </ul>
